def is_garbage_math_response(response: str) -> bool:
    for pat in _GARBAGE_RE:
        if pat.search(response):
            return True
    for tag in re.findall(r'```(\w*)', response):
        if tag.lower() not in _REAL_LANGS:
            return True
    return False


