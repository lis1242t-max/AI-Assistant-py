# ─── Детектор и вычислитель простой арифметики ───────────────────────

_SAFE_CHARS = re.compile(r'^[\d\s\+\-\*\/\(\)\.\%\^x×]+$')


def is_simple_arithmetic(user_message: str):
    msg = user_message.strip()
    # Нормализуем x и × как знаки умножения
    msg_normalized = re.sub(r'(?<!\w)[xх×](?!\w)', '*', msg)
    candidates = []

    # ── Детектор русских степеней: "12 в второй степени", "12 в степени 2", "12 в 2 степени" ──
    _DEGREE_WORDS = {"второй": 2, "второй степени": 2, "2": 2, "третьей": 3, "3": 3,
                     "четвёртой": 4, "четвертой": 4, "4": 4, "пятой": 5, "5": 5,
                     "шестой": 6, "6": 6, "седьмой": 7, "7": 7, "восьмой": 8, "8": 8,
                     "девятой": 9, "9": 9, "десятой": 10, "10": 10}
    m_pow = re.search(
        r'(\d+(?:\.\d+)?)\s*(?:в\s+(?:степени\s+)?|в\s+)(\w+)(?:\s+степени)?',
        msg_normalized, re.IGNORECASE
    )
    if m_pow:
        base_s = m_pow.group(1)
        exp_s = m_pow.group(2).lower()
        if exp_s in _DEGREE_WORDS:
            exp_val = _DEGREE_WORDS[exp_s]
            try:
                base_val = float(base_s)
                result = base_val ** exp_val
                result_str = str(int(result)) if isinstance(result, float) and result.is_integer() else str(result)
                return True, f"{base_s}^{exp_val}"
            except Exception:
                pass

    m = re.search(
        r'(?:сколько\s+(?:будет|равно?)|чему\s+равно?|посчитай|вычисли'
        r'|calculate|compute|what(?:\s+is|\s*=))\s*([\d\s\+\-\*\/\(\)\.\%\^]+)',
        msg_normalized, re.IGNORECASE
    )
    if m:
        candidates.append(m.group(1).strip())

    m2 = re.fullmatch(r'[\d\s\+\-\*\/\(\)\.\%\^]+', msg_normalized)
    if m2:
        candidates.append(msg_normalized)

    for expr in candidates:
        if (
            _SAFE_CHARS.match(expr)
            and any(op in expr for op in ['+', '-', '*', '/', '^', '%'])
            and re.search(r'\d', expr)
            and len(expr) >= 3
        ):
            return True, expr

    return False, ""


def compute_simple_arithmetic(expr: str, language: str = "russian") -> str:
    try:
        # Нормализуем x и × как умножение
        expr = re.sub(r'(?<!\w)[xх×](?!\w)', '*', expr)
        safe_expr = expr.replace('^', '**')
        # Для степеней вида "12^2" которые пришли из детектора
        if not _SAFE_CHARS.match(expr):
            # Попробуем всё равно если expr содержит только цифры и ^
            if not re.match(r'^[\d\.\^]+$', expr):
                return None
        result = eval(safe_expr, {"__builtins__": {}}, {})  # noqa: S307
        if isinstance(result, float) and result.is_integer():
            result_str = str(int(result))
        else:
            result_str = str(result)
        # Красивый вывод: "12² = 144" для степеней
        if '^' in expr:
            parts = expr.split('^')
            if len(parts) == 2:
                base, exp = parts[0].strip(), parts[1].strip()
                sup_map = {'0':'⁰','1':'¹','2':'²','3':'³','4':'⁴','5':'⁵',
                           '6':'⁶','7':'⁷','8':'⁸','9':'⁹'}
                sup = ''.join(sup_map.get(c, c) for c in exp)
                return f"{base}{sup} = {result_str}"
        return f"{expr} = {result_str}"
    except Exception:
        return None


